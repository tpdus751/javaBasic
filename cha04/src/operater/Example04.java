package operater;

public class Example04 {

	public static void main(String[] args) {
		int c = 5;
		System.out.println(c++);
		System.out.println(c);
		System.out.println(++c);

		
		int x = 10;
		System.out.println(x++);
		System.out.println(++x);
		System.out.println(x--);
		System.out.println(--x);
	}

}

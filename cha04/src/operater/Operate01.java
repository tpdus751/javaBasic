package operater;

public class Operate01 {

	public static void main(String[] args) {
		int a = 10, b = 10, c = 10, d = 10;
		
		System.out.println("a++ =>" + (a++));
		System.out.println("a =>" + a);
		System.out.println("++b =>" + (++b));
		System.out.println("c-- =>" + (c--));
		System.out.println("--d =>" + (--d));
		
	
		

	}

}
